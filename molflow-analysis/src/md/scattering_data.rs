/// Compute X-ray atomic form factor f(q) using 5-term Waasmaier-Kirfel coefficients.
///
/// `z` = atomic number (1..=98); returns 0.0 for unknown Z.
/// `q` in Å⁻¹.
pub fn form_factor_xrd(z: usize, q: f64) -> f64 {
    if z == 0 || z > 98 { return 0.0; }
    let (a0,b0,a1,b1,a2,b2,a3,b3,a4,b4,c) = WAASMAIER_KIRFEL[z];
    let s2 = (q / (4.0 * std::f64::consts::PI)).powi(2);
    a0 * (-b0 * s2).exp()
        + a1 * (-b1 * s2).exp()
        + a2 * (-b2 * s2).exp()
        + a3 * (-b3 * s2).exp()
        + a4 * (-b4 * s2).exp()
        + c
}

/// Return neutron coherent scattering length bcoh [fm] for atomic number `z`.
///
/// Returns 0.0 for Z outside 1..=98 or elements without published data.
pub fn neutron_bcoh(z: usize) -> f64 {
    if z == 0 || z > 98 { return 0.0; }
    NEUTRON_BCH[z]
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_form_factor_xrd_at_q0() {
        // f(0) = Σaᵢ + c ≈ Z for neutral atoms (W-K constraint)
        let f_fe = form_factor_xrd(26, 0.0);
        assert!((f_fe - 26.0).abs() < 0.1, "Fe f(0)={:.4}", f_fe);
        let f_o = form_factor_xrd(8, 0.0);
        assert!((f_o - 8.0).abs() < 0.1, "O f(0)={:.4}", f_o);
        let f_h = form_factor_xrd(1, 0.0);
        assert!((f_h - 1.0).abs() < 0.1, "H f(0)={:.4}", f_h);
    }

    #[test]
    fn test_form_factor_xrd_decays_with_q() {
        let f0 = form_factor_xrd(26, 0.0);
        let f10 = form_factor_xrd(26, 10.0);
        let f25 = form_factor_xrd(26, 25.0);
        assert!(f0 > f10, "f(0)={:.3} should > f(10)={:.3}", f0, f10);
        assert!(f10 > f25, "f(10)={:.3} should > f(25)={:.3}", f10, f25);
    }

    #[test]
    fn test_neutron_bcoh_values() {
        assert!((neutron_bcoh(26) - 9.45).abs() < 0.01);  // Fe
        assert!((neutron_bcoh(8) - 5.803).abs() < 0.001); // O
        assert!((neutron_bcoh(1) - (-3.739)).abs() < 0.001); // H (negative)
    }

    #[test]
    fn test_unknown_z_returns_zero() {
        assert_eq!(form_factor_xrd(0, 5.0), 0.0);
        assert_eq!(form_factor_xrd(99, 5.0), 0.0);
        assert_eq!(neutron_bcoh(0), 0.0);
        assert_eq!(neutron_bcoh(99), 0.0);
    }
}
